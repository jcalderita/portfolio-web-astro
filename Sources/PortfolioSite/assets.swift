let tagFilterJS = """
(()=>{
const list=document.getElementById('posts-list');
if(!list)return;
const cards=[...list.children];
const btns=[...document.querySelectorAll('[data-tag]')];
let sel=sessionStorage.getItem('blogTag')||'all';
function filter(tag){
  sel=tag;sessionStorage.setItem('blogTag',tag);
  cards.forEach(c=>{
    const t=[...c.querySelectorAll('[data-info]')].map(b=>b.dataset.info.toLowerCase());
    c.style.display=(tag==='all'||t.includes(tag.toLowerCase()))?'':'none';
  });
  btns.forEach(b=>b.classList.toggle('tag-active',b.dataset.tag===tag));
}
btns.forEach(b=>b.onclick=()=>filter(b.dataset.tag));
filter(sel);
})();
"""

let copyButtonJS = """
